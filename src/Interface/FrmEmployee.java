    public void ShowData() throws SQLException {
        ResultSet result = emp.getAllEMPLOYEE();
        DefaultTableModel tableModel = (DefaultTableModel) tblDanhSachNhanVien.getModel();
        tableModel.setRowCount(0); //clear table
        try {
            while (result.next()) { // nếu còn đọc tiếp được một dòng dữ liệu
                String rows[] = new String[7];
                rows[0] = result.getString(1); // lấy dữ liệu tại cột số 1 
                rows[1] = result.getString(2); // lấy dữ liệu tai cột số 2 
                rows[2] = result.getString(3); // lấy dữ liệu tai cột số 3
                rows[3] = result.getString(5); // lấy dữ liệu tai cột số 5
                rows[4] = result.getString(6); // lấy dữ liệu tai cột số 6
                rows[5] = result.getString(7); // lấy dữ liệu tai cột số 6               
                tableModel.addRow(rows); // đưa dòng dữ liệu vào tableModel
            }
        } catch (SQLException e) {
        }
    }

