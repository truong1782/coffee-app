    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tblDanhSachNhanVien.getSelectedRow();
        int rowcount = tblDanhSachNhanVien.getSelectedRowCount();
        if (rowcount == 0) {
            JOptionPane.showMessageDialog(frame, "Vui lòng chọn nhân viên", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (rowcount > 1) {
            JOptionPane.showMessageDialog(frame, "Vui lòng chỉ chọn một nhân viên", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Object EmployeeID = tblDanhSachNhanVien.getValueAt(index, 0);
                String Fullname = this.txtTenNhanVien.getText();
                String Username = this.txtTenTaiKhoan.getText();
                String Password = String.valueOf(MD5.encrypt(txtMatKhau.getText()));
                String Phone = this.txtPhone.getText();
                String Role = String.valueOf(CmbRole.getSelectedItem());
                String image = this.lblImage.getText();
                if (!CheckData(Fullname, Username, Password, Phone, Role, image)) {
                    return;
                }
                if (Integer.parseInt(EmployeeID.toString()) == 1) {
                    emp.editEmployee(EmployeeID, Fullname, Username, Phone, "Admin", image);
                } else {
                    emp.editEmployee(EmployeeID, Fullname, Username, Phone, Role, image);
                }
                btnLamMoiActionPerformed(evt);
                ShowData();
                JOptionPane.showMessageDialog(frame, "Đã cập nhật thông tin cho nhân viên \"" + Fullname + "\"", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(FrmEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblDanhSachNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachNhanVienMouseClicked

        int index = this.tblDanhSachNhanVien.getSelectedRow();
        String Fullname = this.tblDanhSachNhanVien.getValueAt(index, 1).toString();
        String Username = this.tblDanhSachNhanVien.getValueAt(index, 2).toString();
        String Phone = this.tblDanhSachNhanVien.getValueAt(index, 3).toString();
        CmbRole.addItem(tblDanhSachNhanVien.getValueAt(index, 4).toString());
        CmbRole.removeItemAt(3);

        if (this.tblDanhSachNhanVien.getValueAt(index, 5) == null) {
            ImageIcon ImgIcon = new ImageIcon(getClass().getResource("/Image/user.png"));
            Image newImage = ImgIcon.getImage().getScaledInstance(153, 190, Image.SCALE_SMOOTH);
            ImageIcon Icon = new ImageIcon(newImage);
            lblImage.setIcon(Icon);

        } else {
            String image = this.tblDanhSachNhanVien.getValueAt(index, 5).toString();
            lblImage.setText(image);
            ImageIcon ImgIcon = new ImageIcon(getClass().getResource("/Image/" + image));
            Image newImage = ImgIcon.getImage().getScaledInstance(153, 190, Image.SCALE_SMOOTH);
            ImageIcon Icon = new ImageIcon(newImage);
            lblImage.setIcon(Icon);
        }
        this.txtTenNhanVien.setText(Fullname);
        this.txtTenTaiKhoan.setText(Username);
        this.txtPhone.setText(Phone);
        btnThem.setEnabled(false);

    }//GEN-LAST:event_tblDanhSachNhanVienMouseClicked

    private void txtTimKiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemKeyReleased
        String keyword = txtTimKiem.getText();
        DefaultTableModel tableModel = (DefaultTableModel) tblDanhSachNhanVien.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(tableModel);
        tblDanhSachNhanVien.setRowSorter(tr);

        tr.setStringConverter(new TableStringConverter() {
            @Override
            public String toString(TableModel model, int row, int column) {
                return VNCharacterUtils.removeAccent(model.getValueAt(row, column).toString()).toLowerCase();
            }
        });

        tr.setRowFilter(RowFilter.regexFilter(keyword));
    }//GEN-LAST:event_txtTimKiemKeyReleased

