    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
//         TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter imageFileter = new FileNameExtensionFilter("hình ảnh", "jpg", "png");
        fileChooser.setFileFilter(imageFileter);
        fileChooser.setMultiSelectionEnabled(false);
        int x = fileChooser.showDialog(this, "chọn file");
        if (x == JFileChooser.APPROVE_OPTION) {
            try {
                File choosed_file = fileChooser.getSelectedFile();
                String fileName = "";
                if (choosed_file.toString().contains(".png")) {
                    fileName = txtTenTaiKhoan.getText() + ".png";
                } else {
                    fileName = txtTenTaiKhoan.getText() + ".jpg";
                }
                String file_destination = getClass().getResource("/Image/").getFile() + fileName;
                BufferedImage bi = ImageIO.read(choosed_file);
                ImageIO.write(bi, "jpg", new File(file_destination));

                ImageIcon image = new ImageIcon(file_destination);
                Image newImage = image.getImage().getScaledInstance(153, 190, Image.SCALE_SMOOTH);
                ImageIcon Icon = new ImageIcon(newImage);
                lblImage.setText(fileName);
                lblImage.setIcon(Icon);

            } catch (IOException ex) {
                Logger.getLogger(FrmEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton2ActionPerformed
    }
