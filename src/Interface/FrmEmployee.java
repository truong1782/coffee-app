    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int[] index = tblDanhSachNhanVien.getSelectedRows();

        if (index.length == 0) {
            JOptionPane.showMessageDialog(frame, "Vui lòng chọn nhân viên", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (index.length >= 1) {
            for (int i = 0; i < index.length; i++) {
                Object EmployeeID = tblDanhSachNhanVien.getValueAt(index[i], 0);
                Object productName = tblDanhSachNhanVien.getValueAt(index[i], 1);
                int dialog = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa nhân viên \"" + productName + "\" ra khỏi danh sách ?", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (dialog == JOptionPane.YES_OPTION) {
                    try {
                        emp.deleteEmployee(EmployeeID);
                        btnLamMoiActionPerformed(evt);
                        ShowData();
                        JOptionPane.showMessageDialog(frame, "Đã xóa nhân viên \"" + productName + "\" ra khỏi danh sách", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    } catch (SQLException ex) {
                        Logger.getLogger(FrmEmployee.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

